def get_a_share_theme(symbol: str, industry: str | None) -> str | None:
    """A 股标的 → 友好主题（产业链地图用）。返回 None 表示排除。

    优先级：
      1. STOCK_THEME_MAP 精确命中（值可能是 None，表示主动排除）
      2. industry 前 3 字（"C39"）→ INDUSTRY_FALLBACK
      3. industry 前 3 字 → EXCLUDED_INDUSTRIES：None
      4. 都没命中：None
    """
    if symbol in STOCK_THEME_MAP:
        return STOCK_THEME_MAP[symbol]
    if not industry:
        return None
    code3 = industry[:3]
    if code3 in EXCLUDED_INDUSTRIES:
        return None
    if code3 in INDUSTRY_FALLBACK:
        return INDUSTRY_FALLBACK[code3]
    return None
